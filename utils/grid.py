lava_group = pygame.sprite.Group()
finish_group = pygame.sprite.Group()
        
def build_tile_list(tile_size):
    tile_list = []

    grass_block = pygame.Surface((tile_size, tile_size))
    grass_block.fill((53, 130, 61))

    earth_block = pygame.Surface((tile_size, tile_size))
    earth_block.fill((179, 128, 77))

    platform_block = pygame.Surface((tile_size, tile_size))
    platform_block.fill((91, 134, 134))

    finish_block = pygame.Surface((tile_size, tile_size))
    finish_block.fill((153, 255, 153))

    row_count = 0
    for row in level_layout:
        column_count = 0
        for tile in row:
            if tile == 1:
                grass_block_rect = grass_block.get_rect()
                grass_block_rect.x = column_count * tile_size
                grass_block_rect.y = row_count * tile_size
                tile = (grass_block, grass_block_rect)
                tile_list.append(tile)
            if tile == 2:
                earth_block_rect = earth_block.get_rect()
                earth_block_rect.x = column_count * tile_size
                earth_block_rect.y = row_count * tile_size
                tile = (earth_block, earth_block_rect)
                tile_list.append(tile)
            if tile == 3:
                platform_block_rect = platform_block.get_rect()
                platform_block_rect.x = column_count * tile_size
                platform_block_rect.y = row_count * tile_size
                tile = (platform_block, platform_block_rect)
                tile_list.append(tile)
            if tile == 4:
                lava_block = Lava(column_count * tile_size,
                                   row_count * tile_size, tile_size)
                lava_group.add(lava_block)
            if tile == 5:
                finish_block = Finish(column_count * tile_size,
                                   row_count * tile_size, tile_size)
                finish_group.add(finish_block)
            column_count += 1
        row_count += 1

    return tile_list, lava_group, finish_group

def draw_level(tile_list, screen):
    for tile in tile_list:
        screen.blit(tile[0], tile[1])
        
